package com.vo;

public class EmpInfo {
	
	private String eNo;
	private String eNm;
	private String eId;
	private String ePw;
	private String eDept;
	private String ePosi;
	private String eInDt;
	private String eOutDt;
	
	public String geteNo() {
		return eNo;
	}
	public void seteNo(String eNo) {
		this.eNo = eNo;
	}
	public String geteNm() {
		return eNm;
	}
	public void seteNm(String eNm) {
		this.eNm = eNm;
	}
	public String geteId() {
		return eId;
	}
	public void seteId(String eId) {
		this.eId = eId;
	}
	public String getePw() {
		return ePw;
	}
	public void setePw(String ePw) {
		this.ePw = ePw;
	}
	public String geteDept() {
		return eDept;
	}
	public void seteDept(String eDept) {
		this.eDept = eDept;
	}
	public String getePosi() {
		return ePosi;
	}
	public void setePosi(String ePosi) {
		this.ePosi = ePosi;
	}
	public String geteInDt() {
		return eInDt;
	}
	public void seteInDt(String eInDt) {
		this.eInDt = eInDt;
	}
	public String geteOutDt() {
		return eOutDt;
	}
	public void seteOutDt(String eOutDt) {
		this.eOutDt = eOutDt;
	}
}
